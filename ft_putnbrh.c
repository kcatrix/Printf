#include "ft_printf.h"

int	ft_putnbrh(int nb)
{
	char	c;
	int		i;

	i = 0;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		i += 11;
	}
	if ((nb < 0) && (nb != -2147483648))
	{
		write(1, "-", 1);
		i++;
		nb = nb * -1;
	}
	if (nb >= 0 && nb <= 9)
	{
		c = nb + '0';
		write(1, &c, 1);
		i++;
	}
	if (nb > 9)
	{
		i += ft_putnbr(nb / 16);
		i += ft_putnbr(nb % 16);
	}
	return (i);
}